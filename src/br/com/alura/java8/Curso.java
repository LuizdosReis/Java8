package br.com.alura.java8;

public class Curso {
	private String nome;
	private int QtAlunos;

	public Curso(String nome, int qtAlunos) {
		super();
		this.nome = nome;
		QtAlunos = qtAlunos;
	}

	public String getNome() {
		return nome;
	}

	public int getQtAlunos() {
		return QtAlunos;
	}

}


